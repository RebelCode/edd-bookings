    	<h2>Adding a Bookable Download</h2>
            <img src="<?php echo EDD_BK_IMGS_URL; ?>booking-options.png" />
        <h4>Step 1</h4>
        <p>
            To create your first Booking head over to "Downloads" > "Add New". After entering the required information for the Booking's title, description, categories, tags and so on, you can get right down to the Booking details.
        
            Upon opening the New Download page, scroll down to the "Booking Options" metabox and check the "Enable bookings for this download" checkbox. This will automatically remove all the unrequired metaboxes and display the new Bookings settings.
        </p>
 
        <h4>Step 2</h4>
        <p>
            Start by selecting the session length, the number of sessions each customer can book, as well as the cost per session.
      
            Once the above details have been filled in you can move on to "Schedule". This automatically selects an option titled "Create new schedule and timetable."
      
            Leave this setting as is. The schedule and timetable will automatically be created for you and we can later change the titles and details of the newly created schedule and timetable. 
       
            Last but not least you can choose whether to display the calendar in a multi-view page or post.

        <h4>Step 3</h4>
        <p>
            Once the above details have been filled in you can move on to "Schedule". This automatically selects an option titled "Create new schedule and timetable."
       
            Leave this setting as is. The schedule and timetable will automatically be created for you and we can later change the titles and details of the newly created schedule and timetable. 
        </p>
